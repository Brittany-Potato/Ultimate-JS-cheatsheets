//! Build tools and package managers

// Node.js - Required to run JavaScript outside the browser.
// npm or yarn - Package managers to install dependancies.
// Vite, Webpack, or Parcel - Bundlers for building and optimizing your app.
// ESLint - Linter for maintaining code quality.
// Prettier - Code formatter for consistent style.

//! Frameworks and libraries
// UI Frameworks:
// React, Vue, or Svelte - Popular UI frameworks.
// - npm create vite@latest
// - npm install react react-dom

// Next.js or Nuxt.js - Meta-frameworks for server-side rendering or static site generation.
// Tailwind CSS - Utility-first CSS framework.
// - npm install -D tailwindcss postcss autoprefixer
// - tailwindcss init -p - sets up config files

// Bootstrap, Material UI, or chakra UI - Component libraries.
// - npm install bootstrap
// - npm install @mui/material

// State management:
// Redux, Zustand, Jotai, or Recoil - For managing application state.
// - npm install redux react-redux
// - npm install zustand
// - npm install jotai
// - npm install recoil

// React Query or SWR - For data fetching and caching.
// - npm install @tanstack/react-query
// - npm install sr

//! HTTP Clients
// axios - Popular promise-based http client
// - npm install axios - fetch is also built in

// fetch - Native API, often used with custom wrappers.

//! Testing libraries
// Jest - Testing framework.
// - npm install -D jest

// Testing Library (React Testing Library) - Utilities to test UI components.
// - npm install -D @testing-library/react

// Cypress or playwright - For end - end testing.
// - npm install -D playwright

//! Security and Env tools
// dotenv - For loading enviroment variables.
// - npm install dotenv

// helmet - secures HTTP headers (mainly for express apps)
// - npm install helmet

//! Development utilities
// nodemon - Restarts server auomatically on changes (for backend).
// - npm install -D nodemon

// concurrently - Runs multiple scripts (Like frontend and backend dev servers).
// - npm install -D concurrently

// rimraf - Cross-platform rm -rf.
// - npm install -D rimraf

//! Backend (if applicable)
// Express - Web-framework
// - npm install express

// cors - Middleware for Cross-Origin Resource sharing.
// - npm install cors

// mongoose - MongoDB object modeling tool.
// - npm install mongoose

// sequelize - SQL ORM for Node.js.
// - npm install sequelize
// - npm install pg pg-hstore - If using sequelize with postgreSQL

//! Other useful tools
// classnames - Utility for conditionally joining class names.
// - npm install classnames - conditionally apply class names

// uuid - For generating uqiue IDs.
// - npm install uuid

// lodash - Utility functions for common JavaScript tasks.
// - npm install lodash

// dayjs or date-fns - Date manipulation libraries.
// - npm install dayjs
// - npm install date-fns
